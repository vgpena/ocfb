<h2>...Flaming...?</h2>
<p> Our name and team colors (rainbow) tend to turn heads and invite questions. Our penchant for rainbow is explained by alumna Catie as </p><blockquote>"...basically a play on the general reputation/stereotype that people in the Cleveland area have about Obies -- i.e., that we're all queer hippies. Basically it represents Oberlin's diversity while at the same time making fun of the stereotype, especially since at the time the patch was adopted there was only one person of alternative sexuality on the team".</blockquote>
<p>Our team name, besides being used to strike fear into the hearts of the gods themselves, is a reference to the movie <a href="http://en.wikipedia.org/wiki/Zorro,_The_Gay_Blade" target="_blank">Zorro, the Gay Blade</a>. Yes, we compete in rainbow socks, and yes, we <i>are</i> the prettiest.
</p>

